# somando dois números
# Crie um programa que leia dois números e mostre a soma entre eles

# int, float, str são exemplos de variáveis primitivas
# var = float assim se define o tipo da variável
# print('lorem lorem lorem {}'.format(var)) é uma forma de print da variavel dentro das aspas

n1 = int(input('Digite um valor:'))
n2 = int(input('Digite outro valor:'))
soma = n1+n2

print('a soma entre {} e {} vale {}'.format(n1, n2, soma))
